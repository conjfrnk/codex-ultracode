def validate_runtime_test_shards():
    """Fail closed when a runtime test is missing, unknown, or multiply assigned."""
    expected = {
        name
        for name in dir(RuntimeWorkflowTests)
        if name.startswith("test_") and callable(getattr(RuntimeWorkflowTests, name))
    }
    assigned = [name for shard in SHARD_ORDER for name in RUNTIME_TEST_SHARDS[shard]]
    counts = Counter(assigned)
    duplicates = sorted(name for name, count in counts.items() if count != 1)
    missing = sorted(expected - set(assigned))
    unknown = sorted(set(assigned) - expected)
    unexpected_shards = sorted(set(RUNTIME_TEST_SHARDS) - set(SHARD_ORDER))
    if duplicates or missing or unknown or unexpected_shards:
        detail = "duplicates=%r missing=%r unknown=%r unexpected_shards=%r" % (
            duplicates[:10],
            missing[:10],
            unknown[:10],
            unexpected_shards,
        )
        raise RuntimeError("runtime test shard metadata is invalid: " + detail)
    return {name: len(RUNTIME_TEST_SHARDS[name]) for name in SHARD_ORDER}


def runtime_tests_for(shard):
    validate_runtime_test_shards()
    if shard not in RUNTIME_TEST_SHARDS:
        raise ValueError("unknown runtime test shard %s" % shard)
    return unittest.TestSuite(RuntimeWorkflowTests(name) for name in RUNTIME_TEST_SHARDS[shard])


